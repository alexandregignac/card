package com.game.cardgame.gameplay.dto;

public class RemainingCardsSummary {

    private Integer club;

    private Integer diamond;

    private Integer heart;

    private Integer spade;

    public Integer getClub() {
        return club;
    }
      
    public void setClub(Integer club) {
        this.club = club;
    }

    public Integer getDiamond() {
        return diamond;
    }
      
    public void setDiamond(Integer diamond) {
        this.diamond = diamond;
    }

    public Integer getHeart() {
        return heart;
    }
      
    public void setHeart(Integer heart) {
        this.heart = heart;
    }

    public Integer getSpade() {
        return spade;
    }
      
    public void setSpade(Integer spade) {
        this.spade = spade;
    }
}